// Define the class named kmp3
public class kmp3 {
    public static void main(String[] args) {
        
        // Initialize double variable 'a' with the value 1.6 (conversion factor for kilometers to miles)
        double a = 1.6;
        
        // Initialize double variable 'b' with the value 10.8 (distance in kilometers)
        double b = 10.8;
        
        // Declare a double variable 'd' to store the result of the multiplication
        double d;
        
        // Calculate the distance in miles by multiplying kilometers by the conversion factor
        d = a * b;
        
        // Output the result to the console, showing the distance in miles
        System.out.println("The distance  "+10.8+" km in miles is " + d);
    }
}
