// Define the class named agep1
public class agep1 {
    public static void main(String[] args) {
        
        // Initialize integer variable 'a' with the value 2000 (birth year)
        int a = 2000;
        
        // Initialize integer variable 'b' with the value 2024 (current year)
        int b = 2024;
        
        // Declare an integer variable 'c' to store the age difference
        int c;
        
        // Calculate the age difference by subtracting the birth year (a) from the current year (b)
        c = b - a;
        
        // Output the result to the console, showing the age difference
        System.out.println("Age diff is: " + c);
    }
}
