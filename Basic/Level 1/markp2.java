// Define the class named markp2
public class markp2 {
    public static void main(String[] args) {
        
        // Initialize integer variable 'p' with the value 95 (marks in Physics)
        int p = 95;
        
        // Initialize integer variable 'm' with the value 94 (marks in Maths)
        int m = 94;
        
        // Initialize integer variable 'c' with the value 96 (marks in Chemistry)
        int c = 96;
        
        // Declare an integer variable 'd' to store the average of the marks
        int d;
        
        // Calculate the average by summing the marks and dividing by 3 (number of subjects)
        d = (p + m + c) / 3;
        
        // Output the result to the console, showing the average marks
        System.out.println("The average is " + d);
    }
}
